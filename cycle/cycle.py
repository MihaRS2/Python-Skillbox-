#Даша начала проходить циклы. Она написала программу, которая просто должна считать сумму чисел до тех пор, пока мы не ввели 0, но что-то пошло не так.






number = int(input('Введите число: '))
summa = 0
while number != 0:
   summa += number
   number = int(input('Введите число: '))
print(summa)


 
 
 